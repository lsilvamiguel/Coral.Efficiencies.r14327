//==========================================================================
bool CsRCOptCorr::ExtrapG( int nPt, double* xx, double* yy, double xxe,
//---------------------------------------------------------------------
			   double& yye ) {

// Paolo - September  2008

  double dx10, sx10, dy10, dx21, sx21, dy21;
  double aa0 = 0.;
  double aa1 = 0.;
  double aa2 = 0.;

  if( nPt == 1 ) {
//- equal extep.
    aa2 = 0.;
    aa1 = 0.;
    aa0 = yy[0];
    //std::cout << "aa  " << aa0 << "  " << aa1 << std::endl;
  }

  if( nPt == 2 ) {
//- linear extep.
    dx10 = xx[1] - xx[0];
    dy10 = yy[1] - yy[0];
    if( dx10 == 0.  ||  dy10 == 0. ) return  false;
    aa2 = 0.;
    aa1 = dy10 / dx10;
    aa0 = yy[0] - aa1*xx[0];
    //std::cout << "aa  " << aa0 << "  " << aa1 << std::endl;
  }

  if( nPt == 3 ) {
//- quadratic extep.
    dx10 = xx[1] - xx[0];
    dx21 = xx[2] - xx[1];
    if( dx10 == 0.  ||  dx21 == 0. ) return  false;
    sx10 = xx[1] + xx[0];
    sx21 = xx[2] + xx[1];
    dy10 = yy[1] - yy[0];
    dy21 = yy[2] - yy[1];
    if( dy10 == 0.  ||  dy21 == 0. ) return  false;
    double den = dx21*sx21*dx10 - dx10*sx10*dx21;
    aa2 =   ( dy21*dx10 - dy10*dx21 ) / den;
    //aa1 = - ( dy21*dx10*sx10 - dy10*dx21*sx21 ) / den;
    double aa1a = ( dy10 - aa2 * dx10*sx10 ) / dx10;
    double aa1b = ( dy21 - aa2 * dx21*sx21 ) / dx21;
    aa1 = ( aa1a + aa1b ) /2.;
    double aa0a = yy[0] - aa1*xx[0] -aa2*xx[0]*xx[0];
    double aa0b = yy[1] - aa1*xx[1] -aa2*xx[1]*xx[1];
    double aa0c = yy[2] - aa1*xx[2] -aa2*xx[2]*xx[2];
    aa0 = ( aa0a + aa0b + aa0c ) /3.;
    //aa0 = aa0a;
    //std::cout << "aa  " << aa0 << "  " << aa1 << "  " << aa2 << std::endl;
  }

  yye = aa0 + aa1*xxe + aa2*xxe*xxe;

  //std::cout << xxe << " - ";
  //for( int k=0; k<nPt; k++ ) std::cout << xx[k] << "  ";
  //std::cout << " --- ";
  //std::cout << yye << " - ";
  //for( int k=0; k<nPt; k++ ) std::cout << yy[k] << "  ";
  //std::cout << std::endl;

  return  true;
}


